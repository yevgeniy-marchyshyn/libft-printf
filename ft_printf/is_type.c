#include "ft_printf.h"

int		is_type(char c)
{
	return (c == 's' || c == 'S' || c == 'p' || c == 'd' ||
			c == 'D' || c == 'i' || c == 'o' || c == 'O' ||
			c == 'u' || c == 'U' || c == 'x' || c == 'X' ||
			c == 'c' || c == 'C' || c == '%');
}
